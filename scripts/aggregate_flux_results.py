"""
Aggregate FLUX POC evaluation results and emit:
  - paper/tables/flux_results.tex   (3-row x 4-col table, Markdown + LaTeX)
  - flux_paper_summary.md           (Markdown summary for quick inspection)

Reads results/flux_*_vs_baseline.json (within-FLUX) and optionally
results/flux_anti2_vs_sd21_baseline.json (cross-model).

Usage:
    python scripts/aggregate_flux_results.py
"""
import argparse
import json
import os
from pathlib import Path
from typing import Optional


FLUX_LABELS = {
    "baseline": "FLUX MV-SDI baseline (K=1)",
    "anti2": "FLUX MV-SDI K=2 antithetic",
    "anti4": "FLUX MV-SDI K=4 antithetic",
}

# Per-prompt DiT call budget (matched across all 3 configs by design).
FLUX_DIT_BUDGET = {
    "baseline": "4000 x 7 = 28K",
    "anti2": "2000 x 14 = 28K",
    "anti4": "1000 x 28 = 28K",
}


def load_summary(path: str) -> Optional[dict]:
    if not os.path.exists(path):
        return None
    with open(path) as f:
        return json.load(f)


def fmt_metric(d: Optional[dict], key: str, mult: float = 1.0, fmt: str = ".3f") -> str:
    if d is None: return "--"
    v = d.get(key)
    if v is None: return "--"
    return f"{v * mult:{fmt}}"


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--results-dir", default="results")
    p.add_argument("--out-tex", default="paper/tables/flux_results.tex")
    p.add_argument("--out-md", default="flux_paper_summary.md")
    args = p.parse_args()

    # Within-FLUX: load JSONs comparing anti2/anti4 vs flux_baseline.
    # The baseline_* fields therein refer to FLUX K=1 baseline. We extract:
    #  - baseline (FLUX K=1) numbers from any of the 2 files
    #  - anti2 / anti4 from their respective files
    j_anti2 = load_summary(os.path.join(args.results_dir, "flux_anti2_vs_baseline.json"))
    j_anti4 = load_summary(os.path.join(args.results_dir, "flux_anti4_vs_baseline.json"))
    j_cross = load_summary(os.path.join(args.results_dir, "flux_anti2_vs_sd21_baseline.json"))

    # Baseline FLUX numbers: take from whichever within-FLUX json is present.
    src_baseline = j_anti2 or j_anti4
    if src_baseline is None:
        print(f"ERROR: no FLUX evaluation JSONs found in {args.results_dir}/.")
        print("Run ./scripts/eval_flux.sh first.")
        return

    baseline_summary = src_baseline.get("summary", {})
    base_clip = baseline_summary.get("clip_score", {})
    base_rprec = baseline_summary.get("r_precision", {})
    base_hps = baseline_summary.get("hpsv2", {})
    base_ir = baseline_summary.get("image_reward", {})

    rows = []
    # Baseline row.
    rows.append({
        "name": FLUX_LABELS["baseline"],
        "budget": FLUX_DIT_BUDGET["baseline"],
        "clip": base_clip.get("baseline_mean"),
        "rprec": base_rprec.get("baseline_mean"),
        "hps": base_hps.get("baseline_mean"),
        "ir": base_ir.get("baseline_mean"),
    })
    # anti2 row.
    if j_anti2 is not None:
        s = j_anti2["summary"]
        rows.append({
            "name": FLUX_LABELS["anti2"],
            "budget": FLUX_DIT_BUDGET["anti2"],
            "clip": s.get("clip_score", {}).get("ours_mean"),
            "rprec": s.get("r_precision", {}).get("ours_mean"),
            "hps": s.get("hpsv2", {}).get("ours_mean"),
            "ir": s.get("image_reward", {}).get("ours_mean"),
        })
    # anti4 row.
    if j_anti4 is not None:
        s = j_anti4["summary"]
        rows.append({
            "name": FLUX_LABELS["anti4"],
            "budget": FLUX_DIT_BUDGET["anti4"],
            "clip": s.get("clip_score", {}).get("ours_mean"),
            "rprec": s.get("r_precision", {}).get("ours_mean"),
            "hps": s.get("hpsv2", {}).get("ours_mean"),
            "ir": s.get("image_reward", {}).get("ours_mean"),
        })

    # Find the best per metric (excluding None).
    def best(key):
        vals = [r[key] for r in rows if r[key] is not None]
        return max(vals) if vals else None

    best_clip = best("clip")
    best_rprec = best("rprec")
    best_hps = best("hps")
    best_ir = best("ir")

    # ------------------------------------------------------------ LaTeX --
    n_prompts = baseline_summary.get("num_prompts", 5)
    tex = []
    tex.append("% Auto-generated by scripts/aggregate_flux_results.py")
    tex.append("\\begin{table}[t]")
    tex.append("\\centering")
    tex.append(f"\\caption{{Extension of MV-SDI to FLUX.1-dev (DiT, rectified-flow prior), "
               f"evaluated on {n_prompts} DreamFusion prompts. All configs use a matched "
               f"per-prompt DiT-call budget. Bold = best.}}")
    tex.append("\\label{tab:flux_results}")
    tex.append("\\small")
    tex.append("\\begin{tabular}{l c c c c c}")
    tex.append("\\toprule")
    tex.append("Method & DiT budget & CLIP $\\uparrow$ & R-Prec $\\uparrow$ & HPSv2 $\\uparrow$ & ImageReward $\\uparrow$ \\\\")
    tex.append("\\midrule")
    for r in rows:
        def cell(value, best_value, mult=1.0, suffix=""):
            if value is None: return "--"
            s = f"{value * mult:.3f}{suffix}"
            if best_value is not None and value == best_value:
                return f"\\textbf{{{s}}}"
            return s
        clip_c = cell(r["clip"], best_clip)
        rprec_c = cell(r["rprec"], best_rprec, mult=100.0, suffix="\\%")
        hps_c = cell(r["hps"], best_hps)
        ir_c = cell(r["ir"], best_ir)
        tex.append(f"{r['name']} & {r['budget']} & {clip_c} & {rprec_c} & {hps_c} & {ir_c} \\\\")
    tex.append("\\bottomrule")
    tex.append("\\end{tabular}")
    tex.append("\\end{table}")
    tex_str = "\n".join(tex) + "\n"

    os.makedirs(os.path.dirname(args.out_tex), exist_ok=True)
    with open(args.out_tex, "w") as f:
        f.write(tex_str)

    # ------------------------------------------------------------ Markdown --
    md = []
    md.append("# FLUX POC Results\n")
    md.append(f"- Number of prompts evaluated: **{n_prompts}**")
    md.append(f"- Within-FLUX comparison source: `results/flux_anti2_vs_baseline.json`, `results/flux_anti4_vs_baseline.json`")
    md.append("")
    md.append("## Main table\n")
    md.append("| Method | DiT budget | CLIP | R-Prec | HPSv2 | ImageReward |")
    md.append("|--------|:---:|:---:|:---:|:---:|:---:|")
    for r in rows:
        c = f"{r['clip']:.3f}" if r['clip'] is not None else "--"
        rp = f"{r['rprec']*100:.1f}%" if r['rprec'] is not None else "--"
        h = f"{r['hps']:.3f}" if r['hps'] is not None else "--"
        ir = f"{r['ir']:.3f}" if r['ir'] is not None else "--"
        md.append(f"| {r['name']} | {r['budget']} | {c} | {rp} | {h} | {ir} |")
    md.append("")

    if j_cross is not None:
        s = j_cross["summary"]
        md.append("## Cross-model: FLUX K=2 anti vs SD2.1 baseline (K=1)\n")
        md.append("| Metric | SD2.1 baseline | FLUX MV-SDI K=2 anti | Delta |")
        md.append("|--------|:---:|:---:|:---:|")
        for key, label in [("clip_score", "CLIP"), ("r_precision", "R-Prec"), ("hpsv2", "HPSv2"), ("image_reward", "ImageReward")]:
            entry = s.get(key, {})
            sd = entry.get("baseline_mean")
            fl = entry.get("ours_mean")
            if sd is None or fl is None:
                continue
            mult = 100.0 if key == "r_precision" else 1.0
            suffix = "%" if key == "r_precision" else ""
            md.append(f"| {label} | {sd*mult:.3f}{suffix} | {fl*mult:.3f}{suffix} | {(fl-sd)*mult:+.3f}{suffix} |")
        md.append("")

    md.append("## LaTeX table (`paper/tables/flux_results.tex`)\n")
    md.append("```latex")
    md.append(tex_str.strip())
    md.append("```\n")

    with open(args.out_md, "w") as f:
        f.write("\n".join(md) + "\n")

    print(f"Wrote {args.out_tex}")
    print(f"Wrote {args.out_md}")
    print("")
    print("=== FLUX POC table ===")
    print(tex_str)


if __name__ == "__main__":
    main()
